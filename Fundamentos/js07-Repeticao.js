/**
 * ESTRUTURA DE REPETIÇÃO
 * Permite executar mais de uma vez um mesmo trecho do código
 * até que uma determinada expressão seja verdadeiro ou falso.
 */


// While
let i = 0;

while (i < 10) {
	console.log("contador [ " + i + " ]")
	i++;
}
console.info("Contador 'While' Finalizado\n")

// For
for (let i = 1; i <= 5; ++i) {
	console.log("contador FOR [ " + i + " ]")
}
console.info("Contador 'FOR' Finalizado")